<template>
  <div class="dialog" v-if="open === true" @click.stop='hideDialog'>
    <div @click.stop class="dialog__content">
        <slot></slot>
    </div>
  </div>
</template>

<script>
export default {
    name: 'my-dialog',
    props:  {
        open: {
            type: Boolean,
            default: false
        }
    },
    methods: {
        hideDialog() {
            this.$emit('update:open', false)
        }
    }
}
</script>

<style scoped>
.dialog {
    top: 0;
    bottom: 0;
    right: 0;
    left: 0;
    background: rgba(0, 0, 0, 0.5);
    position: fixed;
    display: flex;
}

.dialog__content {
    margin: auto;
    background: white;
    border-radius: 10px;
    min-height: 100px;
    min-width: 450px;
}
</style>